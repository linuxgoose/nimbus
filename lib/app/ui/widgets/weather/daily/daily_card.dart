import 'package:flutter/material.dart';
import 'package:get/get.dart';
import 'package:intl/intl.dart';
import 'package:nimbus/app/ui/widgets/weather/status/status_weather.dart';
import 'package:nimbus/app/ui/widgets/weather/status/status_data.dart';
import 'package:nimbus/main.dart';

class DailyCard extends StatefulWidget {
  const DailyCard({
    super.key,
    required this.timeDaily,
    required this.weathercodeDaily,
    required this.temperature2MMax,
    required this.temperature2MMin,
  });

  final DateTime timeDaily;
  final int? weathercodeDaily;
  final double? temperature2MMax;
  final double? temperature2MMin;

  @override
  State<DailyCard> createState() => _DailyCardState();
}

class _DailyCardState extends State<DailyCard> {
  final statusWeather = StatusWeather();
  final statusData = StatusData();

  @override
  Widget build(BuildContext context) {
    if (widget.weathercodeDaily == null) {
      return Container();
    }

    return Card(
      margin: const EdgeInsets.symmetric(horizontal: 10, vertical: 8),
      child: Padding(
        padding: const EdgeInsets.symmetric(vertical: 15, horizontal: 20),
        child: Row(
          spacing: 5,
          children: [_buildTemperatureInfo(context), _buildWeatherImage()],
        ),
      ),
    );
  }

  Widget _buildTemperatureInfo(BuildContext context) => Expanded(
    child: Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      spacing: 5,
      children: [
        Text(
          '${statusData.getDegree(widget.temperature2MMin?.round())} / ${statusData.getDegree(widget.temperature2MMax?.round())}',
          style: context.textTheme.titleLarge?.copyWith(
            fontSize: 22,
            fontWeight: FontWeight.w600,
          ),
        ),
        _buildDateText(context),
        _buildWeatherDescription(context),
      ],
    ),
  );

  Widget _buildDateText(BuildContext context) => Text(
    DateFormat.MMMMEEEEd(locale.languageCode).format(widget.timeDaily),
    style: context.textTheme.titleMedium?.copyWith(
      color: Colors.grey,
      fontWeight: FontWeight.w400,
    ),
  );

  Widget _buildWeatherDescription(BuildContext context) => Text(
    statusWeather.getText(widget.weathercodeDaily),
    style: context.textTheme.titleMedium?.copyWith(
      color: Colors.grey,
      fontWeight: FontWeight.w400,
    ),
  );

  Widget _buildWeatherImage() => Icon(
    statusWeather.getIconNowDaily(widget.weathercodeDaily),
    size: 48,
    color: context.theme.colorScheme.primary,
  );
}
